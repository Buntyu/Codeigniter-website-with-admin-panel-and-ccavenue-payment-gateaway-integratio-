<p>Incorrect Address: BISJ does not take responsibility if the delivery address provided by you is incorrect or outdated. If a package is returned to us because the address is incorrect or outdated we will consider the order status as closes, and no refund will be issued. Please carefully check to make sure that you have supplied a current and correct shipping address.</p>

<p>Address Format: If you supply a Post Office Box for your shipping address, Courier Partner is unable to deliver your package. We will try to catch this problem prior to shipping. If a package is returned to us because the address is incorrect or outdated we will consider the order status as closes, and no refund will be issued. Please make sure that you do not supply a Post Office Box address for your shipping address.</p>

<p>Refused by Recipient: If you are sending a gift to someone and they refuse to accept the package because they think it is being sent to them in error, then the package will be returned to us as undeliverable. By informing your gift recipient that they should expect a package you will help us ensure that they receive your gift. We will consider the order status as closes, and no refund will be issued.</p>

